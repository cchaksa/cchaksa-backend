CREATE TABLE IF NOT EXISTS public.course_evaluations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    created_at TIMESTAMP WITH TIME ZONE NULL,
    updated_at TIMESTAMP WITH TIME ZONE NULL,
    student_id UUID NOT NULL,
    course_id BIGINT NOT NULL,
    professor_id BIGINT NOT NULL,
    year INTEGER NOT NULL,
    semester INTEGER NOT NULL,
    review VARCHAR(2000) NULL,
    CONSTRAINT pk_course_evaluations PRIMARY KEY (id),
    CONSTRAINT uk_course_evaluations_student_semester_course_professor
        UNIQUE (student_id, year, semester, course_id, professor_id)
);

CREATE TABLE IF NOT EXISTS public.course_evaluation_tags (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    course_evaluation_id BIGINT NOT NULL,
    tag VARCHAR(64) NOT NULL,
    CONSTRAINT pk_course_evaluation_tags PRIMARY KEY (id),
    CONSTRAINT uk_course_evaluation_tags_evaluation_tag
        UNIQUE (course_evaluation_id, tag)
);

